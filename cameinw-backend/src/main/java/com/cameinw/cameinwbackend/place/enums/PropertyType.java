package com.cameinw.cameinwbackend.place.enums;

/**
 * The PropertyType enum represents the types of properties available for booking.
 * It specifies the different categories of accommodations offered at a place.
 */
public enum PropertyType {
    /**
     * Represents a villa as a type of accommodation.
     */
    VILLA,
    /**
     * Represents a resort as a type of accommodation.
     */
    RESORT,
    /**
     * Represents a campsite as a type of accommodation.
     */
    CAMPSITE,
    /**
     * Represents an apartment as a type of accommodation.
     */
    APARTMENT,
    /**
     * Represents a hotel as a type of accommodation.
     */
    HOTEL,
}
