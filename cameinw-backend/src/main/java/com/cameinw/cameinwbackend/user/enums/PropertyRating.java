package com.cameinw.cameinwbackend.user.enums;

/**
 * The PropertyRating enum represents the different rating values for a property or place.
 * It includes ratings from one star to five stars, as well as an unrated option.
 */
public enum PropertyRating {
    /**
     * Represents a one-star rating for a property.
     */
    ONE_STAR,
    /**
     * Represents a two-star rating for a property.
     */
    TWO_STARS,
    /**
     * Represents a three-star rating for a property.
     */
    THREE_STARS,
    /**
     * Represents a four-star rating for a property.
     */
    FOUR_STARS,
    /**
     * Represents a five-star rating for a property.
     */
    FIVE_STARS,
    /**
     * Represents an unrated property with no specified rating.
     */
    UNRATED
}